
public class PositiveNegative0Ex9 {
	
	    public static void main(String[] args) {
	        int num = -5;

	        String result = (num > 0) ? "Positive"
	                        : (num < 0) ? "Negative"
	                        : "Zero";

	        System.out.println("Number = " + num);
	        System.out.println("Result = " + result);
	    
	}
}
